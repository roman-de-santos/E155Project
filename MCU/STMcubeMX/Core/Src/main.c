// Callback: Half of the ADC buffer is full
void HAL_ADC_ConvHalfCpltCallback(ADC_HandleTypeDef* hadc)
{
    // While the ADC is filling the SECOND half, we copy the FIRST half to SAI
    if (hadc->Instance == ADC1)
    {
        for (int i = 0; i < BUFFER_SIZE / 2; i++)
        {
            sai_buffer[i] = adc_buffer[i];
        }
    }
}

// Callback: The entire ADC buffer is full
void HAL_ADC_ConvCpltCallback(ADC_HandleTypeDef* hadc)
{
    // While the ADC wraps around to fill the FIRST half, we copy the SECOND half to SAI
    if (hadc->Instance == ADC1)
    {
        for (int i = BUFFER_SIZE / 2; i < BUFFER_SIZE; i++)
        {
            sai_buffer[i] = adc_buffer[i];
        }
    }
}

